    public String sincero(float resultado) {
        String retorno = "";
        retorno = Float.toString(resultado); //RETORNO=RESULTADO(CONVERTIDO A STRING).

        //SI EL RESTO DE DIVIDIR RESULTADO/1 ES IGUAL A 0.
        if (resultado % 1 == 0) {
            retorno = retorno.substring(0, retorno.length() - 2); //RECORTAR LOS ULTIMOS DOS NUMEROS. 
        }
        return retorno;
    }

