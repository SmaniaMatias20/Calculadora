    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        this.segundonumero = Float.parseFloat(this.casilla.getText()); //ASIGNAR AL SEGUNDO NUMERO LO QUE ESTA EN PANTALLA.

        switch (this.operador) {
            case "+":
                this.casilla.setText(sincero(this.primernumero + this.segundonumero));
                break; //MOSTRAR EN LA CASILLA LA SUMA DEL PRIMER NUMERO CON EL SEGUNDO. CASTEAR DE STRING A FLOAT.
            case "-":
                this.casilla.setText(sincero(this.primernumero - this.segundonumero));
                break; //MOSTRAR EN LA CASILLA LA RESTA DEL PRIMER NUMERO CON EL SEGUNDO. CASTEAR DE STRING A FLOAT.
            case "*":
                this.casilla.setText(sincero(this.primernumero * this.segundonumero));
                break; //MOSTRAR EN LA CASILLA LA MULTIPLICACION DEL PRIMER NUMERO CON EL SEGUNDO. CASTEAR DE STRING A FLOAT.
            case "/":
                if (this.segundonumero == 0) {
                    this.casilla.setText("NoSeDividePorCero¡BOLUDO!");
                } //SI EL SEGUNDO NUMERO ES 0 MOSTRAR CARTEL.
                else {
                    this.casilla.setText(sincero(this.primernumero / this.segundonumero));
                    break;
                } //MOSTRAR EN LA CASILLA LA DIVISION DEL PRIMER NUMERO CON EL SEGUNDO. CASTEAR DE STRING A FLOAT.

        }


    }//GEN-LAST:event_jButton17ActionPerformed

